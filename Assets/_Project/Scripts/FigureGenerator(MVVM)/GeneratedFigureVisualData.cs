public class GeneratedFigureVisualData
{
    public FigureData Figure { get; }
    public ColorFrameData Frame { get; }
    public GlassesData Glasses { get; }

    public GeneratedFigureVisualData(FigureData figure, ColorFrameData frame, GlassesData glasses)
    {
        Figure = figure;
        Frame = frame;
        Glasses = glasses;
    }
}